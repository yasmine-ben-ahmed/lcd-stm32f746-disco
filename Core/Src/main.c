/**
 * @brief  Dessine le logo ACTIA stylisé
 */
void DrawLogoACTIA(uint8_t darkMode)
{
    uint32_t blockColor = ACTIA_DGRAY;

    /* Rectangle principal du bloc ACTIA */
    BSP_LCD_SetTextColor(blockColor);
    BSP_LCD_FillRect(20, 14, 108, 38);

    /* Bord gauche vert du bloc */
    BSP_LCD_SetTextColor(ACTIA_GREEN);
    BSP_LCD_FillRect(20, 14, 4, 38);

    /* Texte ACTIA */
    BSP_LCD_SetFont(&Font20);
    BSP_LCD_SetBackColor(blockColor);
    BSP_LCD_SetTextColor(0xFFFFFFFF);
    BSP_LCD_DisplayStringAt(30, 23, (uint8_t *)"ACTIA", LEFT_MODE);

    /* ── Grille 3x3 : 9 carrés identiques ──
       Zone disponible : y=14 à y=52 (h=38), x départ=134
       Taille carré    : 10px
       Gap             : 4px
       3 rangées × (10+4) - 4 = 38px  → tient exactement dans h=38  */

    #define SQ_SIZE  10
    #define SQ_GAP    4
    #define SQ_X0   134
    #define SQ_Y0    14   /* aligné sur le bord haut du bloc */

    BSP_LCD_SetTextColor(ACTIA_GREEN);

    for (uint8_t row = 0; row < 3; row++)
    {
        for (uint8_t col = 0; col < 3; col++)
        {
            BSP_LCD_FillRect(
                SQ_X0 + col * (SQ_SIZE + SQ_GAP),
                SQ_Y0 + row * (SQ_SIZE + SQ_GAP),
                SQ_SIZE,
                SQ_SIZE
            );
        }
    }

    #undef SQ_SIZE
    #undef SQ_GAP
    #undef SQ_X0
    #undef SQ_Y0
}
/**
 * @brief  Dessine le bouton toggle dark/light mode
 */
void DrawToggleButton(uint8_t darkMode)
{
    uint32_t trackColor, knobColor, iconBg;

    if (darkMode) {
        trackColor = ACTIA_GREEN;
        knobColor  = 0xFFFFFFFF;
        iconBg     = DARK_SURFACE;
    } else {
        trackColor = 0xFFCCD5DE;
        knobColor  = 0xFFFFFFFF;
        iconBg     = LIGHT_SURFACE;
    }

    /* Fond du bouton zone */
    BSP_LCD_SetTextColor(iconBg);
    BSP_LCD_FillRect(BTN_X - 4, BTN_Y - 4, BTN_W + 8, BTN_H + 8);

    /* Track (fond du toggle) */
    BSP_LCD_SetTextColor(trackColor);
    BSP_LCD_FillRect(BTN_X, BTN_Y, BTN_W, BTN_H);

    /* Coins arrondis simulés */
    BSP_LCD_SetTextColor(iconBg);
    BSP_LCD_FillRect(BTN_X,              BTN_Y,              4, 4);
    BSP_LCD_FillRect(BTN_X + BTN_W - 4, BTN_Y,              4, 4);
    BSP_LCD_FillRect(BTN_X,              BTN_Y + BTN_H - 4, 4, 4);
    BSP_LCD_FillRect(BTN_X + BTN_W - 4, BTN_Y + BTN_H - 4, 4, 4);

    /* Knob (cercle blanc) */
    BSP_LCD_SetTextColor(knobColor);
    if (darkMode) {
        /* Knob à droite → dark activé */
        BSP_LCD_FillRect(BTN_X + BTN_W - 22, BTN_Y + 3, 18, 20);
    } else {
        /* Knob à gauche → light activé */
        BSP_LCD_FillRect(BTN_X + 4, BTN_Y + 3, 18, 20);
    }

    /* Icône soleil ou lune en petit texte */
    BSP_LCD_SetFont(&Font12);
    if (darkMode) {
        BSP_LCD_SetBackColor(ACTIA_GREEN);
        BSP_LCD_SetTextColor(0xFF1B2A3A);
        BSP_LCD_DisplayStringAt(BTN_X + 6, BTN_Y + 7, (uint8_t *)"*", LEFT_MODE);
        BSP_LCD_SetBackColor(knobColor);
        BSP_LCD_SetTextColor(ACTIA_GREEN);
        BSP_LCD_DisplayStringAt(BTN_X + BTN_W - 19, BTN_Y + 7, (uint8_t *)"D", LEFT_MODE);
    } else {
        BSP_LCD_SetBackColor(trackColor);
        BSP_LCD_SetTextColor(0xFFFFFFFF);
        BSP_LCD_DisplayStringAt(BTN_X + BTN_W - 16, BTN_Y + 7, (uint8_t *)"o", LEFT_MODE);
        BSP_LCD_SetBackColor(knobColor);
        BSP_LCD_SetTextColor(0xFFCC8800);
        BSP_LCD_DisplayStringAt(BTN_X + 6, BTN_Y + 7, (uint8_t *)"L", LEFT_MODE);
    }
}

